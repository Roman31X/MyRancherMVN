    public void Graficar(){
        Connection conexion = null;
        
        try{
        conexion = Conexion.getConnection();
        if(conexion.getAutoCommit()){
            conexion.setAutoCommit(false);
        }
        todoProducto = new ProductoGanadoJDBC();
        listaProducto = todoProducto.seleccionar();
        
        conexion.commit();
        } catch (SQLException ex) {
            ex.printStackTrace(System.out);
            System.out.println("Sucedio un error al rollback");
            try {
                conexion.rollback();
            } catch (SQLException exx) {
                exx.printStackTrace(System.out);
            }
        }
        int i=1;
         barras = new DefaultCategoryDataset();
        for (ProductoGanadoDTO lista : listaProducto) {
//            data.add(lista.getIdproduccionGanado());
                double decimal = Double.parseDouble(lista.getGanancia());
                int numero = (int)decimal;
            barras.addValue( numero, lista.getProducto(), lista.getMes());
        }
//        for (int j = 0; j < data.size(); j++) {
//            barras.addValue(data.get(i) , "Peso ["+(i+1)+"]", "Vaca ["+(i+1)+"]");
//            
//        }
        
        
        
//        for (int i = 0; i < 10; i++) {
//            barras.addValue(i, "Producto", "Mes");            
//        }
        JFreeChart grafico = ChartFactory.createBarChart(
                                    "TABLA DE GANCIAS POR PRODUCTO",//nombre de la grafica
                                    "Meses",//nombre de las barras columnas
                                    "Montos de Ganancia",//nombre de la numeraciÃ³n
                                    barras,//datos de la grafica
                                    PlotOrientation.VERTICAL,//orientacion de la grafica
                                    false,//leyenda de barras individuales
                                    true, //herramientas
                                    false// url de la grafica
                );
        ChartPanel panel = new ChartPanel(grafico);
                            panel.setMouseWheelEnabled(true);
                            panel.setPreferredSize(new Dimension(200, 150));
                            //Designamos medinate los objetos donde se mostrara la grafica
                            jPanel1.setLayout(new BorderLayout());
                            jPanel1.add(panel, BorderLayout.NORTH);
                            
    }
